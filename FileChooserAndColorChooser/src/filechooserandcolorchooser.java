    private void openFolderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_openFolderActionPerformed
        JFileChooser fc = new JFileChooser();
        int i = fc.showOpenDialog(this);     
        
        if(i == JFileChooser.APPROVE_OPTION){
            File file = fc.getSelectedFile();
            String text="";
            try(Scanner ss = new Scanner(new BufferedReader(new FileReader(file)))){
                while(ss.hasNextLine()){
                    text += ss.nextLine() + "\n";
                }
                writePlace.setText(text);
            } catch (FileNotFoundException ex) {
                Logger.getLogger(filechooserandcolorchooser.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_openFolderActionPerformed

