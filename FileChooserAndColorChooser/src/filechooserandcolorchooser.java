    private void darkModActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_darkModActionPerformed
        darkcount += 1;
        
        if(darkcount %2 == 0){
            writePlace.setBackground(Color.black);
            writePlace.setForeground(Color.white);
            
            getContentPane().setBackground(Color.black);
            clear.setBackground(Color.black);
            openFolder.setBackground(Color.black);
            changeColor.setBackground(Color.black);
            darkMod.setBackground(Color.black);
            
            clear.setForeground(Color.white);
            openFolder.setForeground(Color.white);
            changeColor.setForeground(Color.white);
            darkMod.setForeground(Color.white);
            
        }
        else{
            writePlace.setBackground(Color.white);
            writePlace.setForeground(Color.black);
            getContentPane().setBackground(Color.lightGray);
            
            clear.setBackground(Color.lightGray);
            openFolder.setBackground(Color.lightGray);
            changeColor.setBackground(Color.lightGray);
            darkMod.setBackground(Color.lightGray);
            
            clear.setForeground(Color.black);
            openFolder.setForeground(Color.black);
            changeColor.setForeground(Color.black);
            darkMod.setForeground(Color.black);
        }
    }//GEN-LAST:event_darkModActionPerformed

